::pos::ui::button::ATextButton::ATextButton(
    ::QMainWindow& window,
    const ::std::string& text,
    ::std::size_t xPos,
    ::std::size_t yPos,
    ::std::size_t xSize,
    ::std::size_t ySize
) noexcept
    : ::QPushButton{ ::QString{ text.c_str() }, &window }
{
    this->move(static_cast<int>(xPos), static_cast<int>(yPos));
    this->resize(static_cast<int>(xSize), static_cast<int>(ySize));
    this->show();
}



